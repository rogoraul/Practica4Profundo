ej2_code = (
    "# Ejercicio 2: Generar con etiquetas - GAN Condicional (cGAN) sobre MNIST\n"
    "from keras.datasets import mnist\n"
    "from keras.layers import Input, Dense, Reshape, Flatten, BatchNormalization, LeakyReLU, Concatenate\n"
    "from keras.models import Model\n"
    "from keras.optimizers import Adam\n"
    "import tensorflow as tf\n"
    "import numpy as np\n"
    "from tqdm.notebook import tqdm\n"
    "import matplotlib.pyplot as plt\n"
    "\n"
    "# --- Parametros ---\n"
    "latent_cgan = 100; num_classes = 10\n"
    "img_shape_cg = (28, 28, 1); img_flat_cg = int(28*28*1)\n"
    "\n"
    "# --- Datos ---\n"
    "(X_tr_cg, y_tr_cg), (_, _) = mnist.load_data()\n"
    "X_tr_cg = X_tr_cg.astype(np.float32) / 255.0 * 2.0 - 1.0\n"
    "X_tr_cg = np.expand_dims(X_tr_cg, -1)\n"
    "y_onehot = np.eye(num_classes)[y_tr_cg]  # (60000, 10)\n"
    "\n"
    "# --- Generador condicional ---\n"
    "noise_in   = Input(shape=(latent_cgan,))\n"
    "label_in_g = Input(shape=(num_classes,))\n"
    "x_g = Concatenate()([noise_in, label_in_g])\n"
    "x_g = Dense(256)(x_g);  x_g = LeakyReLU(0.2)(x_g); x_g = BatchNormalization(momentum=0.8)(x_g)\n"
    "x_g = Dense(512)(x_g);  x_g = LeakyReLU(0.2)(x_g); x_g = BatchNormalization(momentum=0.8)(x_g)\n"
    "x_g = Dense(1024)(x_g); x_g = LeakyReLU(0.2)(x_g); x_g = BatchNormalization(momentum=0.8)(x_g)\n"
    "x_g = Dense(img_flat_cg, activation='tanh')(x_g)\n"
    "img_out = Reshape(img_shape_cg)(x_g)\n"
    "cgen = Model([noise_in, label_in_g], img_out, name='cgan_generator')\n"
    "cgen.summary()\n"
    "\n"
    "# --- Discriminador condicional ---\n"
    "img_in    = Input(shape=img_shape_cg)\n"
    "label_in_d = Input(shape=(num_classes,))\n"
    "x_d = Flatten()(img_in)\n"
    "x_d = Concatenate()([x_d, label_in_d])\n"
    "x_d = Dense(512)(x_d); x_d = LeakyReLU(0.2)(x_d)\n"
    "x_d = Dense(256)(x_d); x_d = LeakyReLU(0.2)(x_d)\n"
    "validity = Dense(1, activation='sigmoid')(x_d)\n"
    "cdisc = Model([img_in, label_in_d], validity, name='cgan_discriminator')\n"
    "cdisc.summary()\n"
    "cdisc.compile(loss='binary_crossentropy', optimizer=Adam(2e-4, beta_1=0.5), metrics=['accuracy'])\n"
    "\n"
    "# --- Modelo combinado ---\n"
    "cdisc.trainable = False\n"
    "noise_comb = Input(shape=(latent_cgan,))\n"
    "label_comb = Input(shape=(num_classes,))\n"
    "cgan_model = Model([noise_comb, label_comb],\n"
    "                   cdisc([cgen([noise_comb, label_comb]), label_comb]),\n"
    "                   name='cgan_combined')\n"
    "cgan_model.compile(loss='binary_crossentropy', optimizer=Adam(2e-4, beta_1=0.5))\n"
    "cdisc.trainable = True\n"
    "\n"
    "# --- Entrenamiento ---\n"
    "n_tr_cg = 20000; ep_cg = 100; bs_cg = 128\n"
    "DD_cg, GG_cg = [], []\n"
    "pb = tqdm(range(ep_cg), desc='Training cGAN')\n"
    "for epoch in pb:\n"
    "    g_l, d_l, d_a = 0.0, 0.0, 0.0\n"
    "    n_b = n_tr_cg // bs_cg\n"
    "    for _ in range(n_b):\n"
    "        idx = np.random.choice(len(X_tr_cg), bs_cg, replace=False)\n"
    "        imgs_real = X_tr_cg[idx]; labels_real = y_onehot[idx]\n"
    "        fake_labels = np.eye(num_classes)[np.random.randint(0, num_classes, bs_cg)]\n"
    "        imgs_fake = cgen.predict([np.random.normal(0,1,(bs_cg, latent_cgan)), fake_labels], verbose=0)\n"
    "        cdisc.trainable = True\n"
    "        dr = cdisc.train_on_batch([imgs_real, labels_real], np.ones((bs_cg,1)))\n"
    "        df = cdisc.train_on_batch([imgs_fake, fake_labels], np.zeros((bs_cg,1)))\n"
    "        d_l += 0.5*(dr[0]+df[0]); d_a += 0.5*(dr[1]+df[1])\n"
    "        cdisc.trainable = False\n"
    "        gen_lab = np.eye(num_classes)[np.random.randint(0, num_classes, bs_cg)]\n"
    "        g_l += cgan_model.train_on_batch(\n"
    "            [np.random.normal(0,1,(bs_cg, latent_cgan)), gen_lab], np.ones((bs_cg,1)))\n"
    "    d_l /= n_b; d_a /= n_b; g_l /= n_b\n"
    "    pb.set_description(f'cGAN gen={g_l:.4f} disc={d_l:.4f} acc={d_a:.4f}')\n"
    "    DD_cg.append(d_l); GG_cg.append(g_l)\n"
    "\n"
    "plt.plot(DD_cg, label='Discriminador'); plt.plot(GG_cg, label='Generador')\n"
    "plt.legend(); plt.title('cGAN - Curvas de aprendizaje'); plt.show()\n"
    "\n"
    "# Visualizar: un digito por clase\n"
    "noise_vis = np.random.normal(0, 1, (num_classes, latent_cgan))\n"
    "gen_vis = cgen.predict([noise_vis, np.eye(num_classes)], verbose=0)\n"
    "gen_vis = (gen_vis + 1.0) / 2.0\n"
    "fig, axes = plt.subplots(1, num_classes, figsize=(20, 2))\n"
    "for i in range(num_classes):\n"
    "    axes[i].imshow(gen_vis[i,:,:,0], cmap='gray'); axes[i].set_title(str(i)); axes[i].axis('off')\n"
    "plt.suptitle('cGAN: un digito por clase'); plt.tight_layout(); plt.show()\n"
)

