    "\n"
    "# --- Generador ---\n"
    "model_gen_c = Sequential(name='generator_cifar')\n"
    "model_gen_c.add(Input((latent_dim,)))\n"
    "model_gen_c.add(Dense(256));  model_gen_c.add(LeakyReLU(negative_slope=0.2)); model_gen_c.add(BatchNormalization(momentum=0.8))\n"
    "model_gen_c.add(Dense(512));  model_gen_c.add(LeakyReLU(negative_slope=0.2)); model_gen_c.add(BatchNormalization(momentum=0.8))\n"
    "model_gen_c.add(Dense(1024)); model_gen_c.add(LeakyReLU(negative_slope=0.2)); model_gen_c.add(BatchNormalization(momentum=0.8))\n"
    "model_gen_c.add(Dense(int(np.prod(in_shape_c)), activation='tanh'))\n"
    "model_gen_c.add(Reshape(in_shape_c))\n"
    "model_gen_c.summary()\n"
    "\n"
    "# --- Discriminador (mas ancho para manejar 32x32x3) ---\n"
    "model_disc_c = Sequential(name='discriminator_cifar')\n"
    "model_disc_c.add(Input(in_shape_c))\n"
    "model_disc_c.add(Flatten())\n"
    "model_disc_c.add(Dense(512));  model_disc_c.add(LeakyReLU(negative_slope=0.2))\n"
    "model_disc_c.add(Dense(256));  model_disc_c.add(LeakyReLU(negative_slope=0.2))\n"
    "model_disc_c.add(Dense(1, activation='sigmoid'))\n"
    "model_disc_c.summary()\n"
    "model_disc_c.compile(loss='binary_crossentropy', optimizer=opt_dis_c, metrics=['accuracy'])\n"
    "\n"
    "# --- Modelo combinado ---\n"
    "model_disc_c.trainable = False\n"
    "model_gan_c = Sequential(name='gan_cifar')\n"
    "model_gan_c.add(model_gen_c); model_gan_c.add(model_disc_c)\n"
    "model_gan_c.compile(loss='binary_crossentropy', optimizer=opt_gen_c)\n"
    "model_disc_c.trainable = True\n"
    "\n"
    "# --- Entrenamiento ---\n"
    "DD_loss_c, GG_loss_c = [], []\n"
