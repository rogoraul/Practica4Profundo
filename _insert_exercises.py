    "in_shape_c = X_train_c.shape[1:]  # (32, 32, 3)\n"
    "latent_dim = 100\n"
    "\n"
    "# --- Optimizadores ---\n"
    "opt_gen_c = Adam(learning_rate=2e-4, beta_1=0.5, weight_decay=8e-8)\n"
